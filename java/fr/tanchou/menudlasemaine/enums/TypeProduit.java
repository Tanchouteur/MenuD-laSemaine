package fr.tanchou.menudlasemaine.enums;

/**
 * Enum representing the different types of products in a meal.
 * This enum is used to categorize the types of food items such as meats, vegetables, and more.
 */
public enum TypeProduit {

    /** Represents meat products. */
    VIANDE,

    /** Represents carbohydrate-based products, like pasta or rice. */
    FECULENT,

    /** Represents vegetable products. */
    LEGUME,

    /** Represents a complete dish that could be a full meal in itself. */
    PLATCOMPLET,

    /** Represents entry dishes, typically served as the first course. */
    ENTREE
}